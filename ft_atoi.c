#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int	a;
	int s;

	i = 0;
	a = 0;
	s = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		i++;
		if (str[i - 1] == '-')
			s = -1;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		a = a * 10 + (str[i] - '0');
		i++;
	}
	return (a * s);
}
