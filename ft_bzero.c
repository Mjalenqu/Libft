#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t i;

	i = 0;
	while (((unsigned char*)s)[i])
	{
		if (i < n)
			((unsigned char*)s)[i] = '\0';
		i++;
	}
}
